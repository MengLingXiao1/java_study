package com.study.day01.demo01;

public class Demo01Math {
    public static void main(String[] args) {
        //绝对值函数
        System.out.println(Math.abs(3.14));
        System.out.println(Math.abs(0));
        System.out.println(Math.abs(-2.14));
        System.out.println("==============");

        //向上取整
        System.out.println(Math.ceil(3.999));
        System.out.println("==============");

        //向下取整
        System.out.println(Math.floor(3.0001));
        System.out.println("==============");

        //四舍五入
        System.out.println(Math.round(3.5001));

    }
}
